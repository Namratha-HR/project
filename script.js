// Gather live context to send alongside user question
async function gatherLiveContext(userText) {
  const lower = userText.toLowerCase();
  let context = `You are HazardGuard AI, a disaster awareness assistant for Nepal. 
Current district selected: ${currentDistrict}. Today: ${new Date().toLocaleDateString()}.
You help users understand earthquake, flood, landslide risks and give preparedness advice.
Keep responses concise (3-5 sentences max). Use bullet points when listing steps.`;

  // Fetch relevant live data based on what user is asking
  try {
    if (
      lower.includes("weather") ||
      lower.includes("temperature") ||
      lower.includes("rain")
    ) {
      const district = extractDistrict(userText) || currentDistrict;
      const w = await fetchWeather(district);
      if (w)
        context += `\n\nLIVE WEATHER for ${district}: ${w.temp}°C, ${w.condition}, humidity ${w.humidity}%, wind ${w.wind} km/h, feels like ${w.feelsLike}°C.`;
    }
    if (
      lower.includes("air") ||
      lower.includes("aqi") ||
      lower.includes("pollution") ||
      lower.includes("dust")
    ) {
      const district = extractDistrict(userText) || currentDistrict;
      const a = await fetchAirQuality(district);
      if (a)
        context += `\n\nLIVE AIR QUALITY for ${district}: US AQI ${a.aqi} (${a.level}), PM2.5: ${a.pm25} µg/m³.`;
    }
    if (
      lower.includes("earthquake") ||
      lower.includes("quake") ||
      lower.includes("seismic") ||
      lower.includes("bhukamp")
    ) {
      const quakes = await fetchEarthquakes();
      if (quakes.length > 0) {
        const list = quakes
          .slice(0, 5)
          .map((q) => `M${q.mag} at ${q.place} (${q.time})`)
          .join("; ");
        context += `\n\nLIVE EARTHQUAKES in Nepal (last 24h): ${list}.`;
      } else {
        context += `\n\nNo significant earthquakes (M≥2.5) detected in Nepal in the last 24 hours.`;
      }
    }
    if (
      lower.includes("flood") ||
      lower.includes("river") ||
      lower.includes("water") ||
      lower.includes("bahi")
    ) {
      const district = extractDistrict(userText) || currentDistrict;
      const f = await fetchFlood(district);
      context += `\n\nFLOOD DATA for ${district}: River ${f.river} at ${f.level}m (danger level: ${f.danger}m). Status: ${f.status}.`;
    }
  } catch (e) {
    /* context still useful without live data */
  }

  return context;
}

function extractDistrict(text) {
  // Try to find a known district name in the text
  const known = [
    "Kathmandu",
    "Bhaktapur",
    "Lalitpur",
    "Pokhara",
    "Biratnagar",
    "Nepalgunj",
    "Dhangadhi",
    "Bharatpur",
    "Janakpur",
    "Butwal",
    "Hetauda",
    "Dharan",
    "Gorkha",
    "Kaski",
    "Sindhupalchok",
    "Nuwakot",
    "Dolakha",
    "Rasuwa",
    "Kavrepalanchok",
    "Saptari",
    "Bardiya",
    "Kailali",
    "Kanchanpur",
    "Surkhet",
    "Jajarkot",
    "Dang",
    "Salyan",
    "Solukhumbu",
  ];
  for (const d of known) {
    if (text.toLowerCase().includes(d.toLowerCase())) return d;
  }
  const match = text.match(/in ([A-Z][a-z]+)/);
  return match ? match[1] : null;
}

async function processUserInput(input) {
  // Add to history
  chatHistory.push({ role: "user", content: input });

  addTypingIndicator();

  try {
    const systemPrompt = await gatherLiveContext(input);

    const response = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        model: "claude-sonnet-4-20250514",
        max_tokens: 1000,
        system: systemPrompt,
        messages: chatHistory,
      }),
    });

    const data = await response.json();
    const reply =
      data.content?.[0]?.text ||
      "Sorry, I couldn't get a response. Please try again.";

    chatHistory.push({ role: "assistant", content: reply });
    // Keep history to last 10 messages to avoid token overflow
    if (chatHistory.length > 10) chatHistory.splice(0, 2);

    removeTypingIndicator();
    addMessage(reply, "bot");
  } catch (e) {
    removeTypingIndicator();
    // Graceful fallback with keyword responses
    const lower = input.toLowerCase();
    let reply =
      "I'm having trouble connecting. Please check your internet and try again.";
    if (lower.includes("earthquake"))
      reply =
        "Nepal sits on major fault lines. The Kathmandu Valley, Gorkha, Sindhupalchok and surrounding districts are highest risk. Always Drop, Cover, and Hold On during shaking.";
    else if (lower.includes("flood"))
      reply =
        "Terai districts (Saptari, Bardiya, Kailali, Jhapa) face highest flood risk during monsoon (June–September). Move to higher ground immediately if water levels rise.";
    else if (lower.includes("landslide"))
      reply =
        "Hilly and mountain districts like Sindhupalchok, Gorkha, Kaski, and Solukhumbu face high landslide risk, especially after heavy rain. Avoid steep slopes during monsoon.";
    else if (lower.includes("emergency") || lower.includes("help"))
      reply =
        "🆘 Emergency numbers:\n• Police: 100\n• Fire: 101\n• Ambulance: 102\n• Disaster Helpline: 1133";
    else if (lower.includes("prepare") || lower.includes("kit"))
      reply =
        "Emergency kit essentials:\n• 3-day water supply (1L/person/day)\n• Non-perishable food\n• First aid kit\n• Flashlight & batteries\n• Important documents copy\n• Emergency contacts list";
    addMessage(reply, "bot");
  }
}

